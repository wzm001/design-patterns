package com.wzm.pattern.creation;

/*
* 创建型模式，提供了创建对象的机制，能够提升已有代码的灵活性和可复用性。
* @see https://refactoringguru.cn/design-patterns/creational-patterns
*
* 主要包含这几种模式：
* 1. 工厂方法 Factory Method：在父类中提供一个创建对象的方法， 允许子类决定实例化对象的类型。
* 2. 抽象工厂 Abstract Factory
* 3. 生成器 Builder
* 4. 原型 Prototype
* 5. 单例 Singleton
* */